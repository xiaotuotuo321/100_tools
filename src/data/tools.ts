// 导出所有工具的扁平化数组
export const allTools: Tool[] = toolsData.flatMap(category => category.tools)

// 导出特色工具
export const featuredTools: Tool[] = allTools.filter(tool => tool.featured)

// 根据ID查找工具
export function getToolById(id: string): Tool | undefined {
  return allTools.find(tool => tool.id === id)
}

// 根据分类ID查找分类
export function getCategoryById(id: string): ToolCategory | undefined {
  return toolsData.find(category => category.id === id)
}

// 搜索工具
export function searchTools(query: string): Tool[] {
  if (!query.trim()) return []

  const lowercaseQuery = query.toLowerCase()
  return allTools.filter(tool =>
    tool.name.toLowerCase().includes(lowercaseQuery) ||
    tool.description.toLowerCase().includes(lowercaseQuery) ||
    tool.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  )
} 